package com.example.medicinesearch;

public class MyItem {

    private String medicine_picture;
    private String medicine_name;
    private String medicine_text;

    public MyItem() {
        this.medicine_picture = medicine_picture;
        this.medicine_name = medicine_name;
        this.medicine_text = medicine_text;
    }

    public String getMedicine_picture() {
        return medicine_picture;
    }

    public void setMedicine_picture(String medicine_picture) {
        this.medicine_picture = medicine_picture;
    }

    public String getMedicine_name() {
        return medicine_name;
    }

    public void setMedicine_name(String medicine_name) {
        this.medicine_name = medicine_name;
    }

    public String getMedicine_text() {
        return medicine_text;
    }

    public void setMedicine_text(String medicine_text) {
        this.medicine_text = medicine_text;
    }
}
